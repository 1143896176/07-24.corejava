package 案例_选择题案例;

public abstract class Question {
	
	protected String title;
	
	public Question() {
		
	}

	public Question(String title) {
		this.title = title;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
	
	

}
